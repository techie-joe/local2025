/* =============================================================



/* =============================================================
CONFIGS
============================================================= */

const _src_site = '_site/**/*';
const _dep_site = '../';

/* =============================================================
IMPORTS
============================================================= */

const { src, dest } = require('gulp');
const log = console.log;

/* =============================================================
TASKS
============================================================= */

// to copy every files from _src to _dest
function deploy() {
  log(`Deploying site to : ${_dep_site}`);
  return src(_src_site, { dot: true })
  .pipe(dest(_dep_site));
}

module.exports = { deploy };